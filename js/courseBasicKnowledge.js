function esc(s) {
  return String(s)
    .replace(/&/g, "&amp;")
    .replace(/</g, "&lt;")
    .replace(/>/g, "&gt;")
    .replace(/"/g, "&quot;");
}

/**
 * 规则页「课程体系与学科特点」HTML（均为常量）
 */
export function buildRulesCourseBasicHtml() {
  const sectionHtml = COURSE_BASIC_SECTIONS.map((sec) => {
    const paras = (sec.paragraphs || [])
      .map((p) => `<p>${esc(p)}</p>`)
      .join("");
    const bullets = sec.bullets?.length
      ? `<ul>${sec.bullets.map((b) => `<li>${esc(b)}</li>`).join("")}</ul>`
      : "";
    const subs = (sec.sub || [])
      .map(
        (sub) =>
          `<section class="rules-kb-sub"><h5>${esc(sub.title)}</h5><ul>${sub.bullets
            .map((b) => `<li>${esc(b)}</li>`)
            .join("")}</ul></section>`
      )
      .join("");
    return `<section class="rules-kb-block"><h4>${esc(sec.title)}</h4>${paras}${bullets}${subs}</section>`;
  }).join("");
  return `<div class="rules-course-basic">${sectionHtml}</div>`;
}
